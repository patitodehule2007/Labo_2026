package A01_intro;

public class IngresoDatos {
    public static void main(String[] args) {
        int N = 3;
        double A = 5;
        char C = 'd';
        System.out.println("N: " + N);
        System.out.println("A: " + A);
        System.out.println("C: " + C);
        System.out.println("A + N: " + (A + N));
        System.out.println("A - N: " + (A-N));
        System.out.println("NUM(C) : " + (int)(C) );
    }
}
