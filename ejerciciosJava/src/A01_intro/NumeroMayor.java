package A01_intro;

public class NumeroMayor {
    public static void main(String[] args) {
        double A = 6;
        double B = 8;

        if(A > B){
            System.out.println("A es mayor que B");
        }else if(B > A){
            System.out.println("B es mayor que A");
        }
        else {
            System.out.println("B es igual que A");
        }
    }
}
